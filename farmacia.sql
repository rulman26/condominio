CREATE TABLE ITEM(
ITEM_ID INT NOT NULL PRIMARY KEY auto_increment,
ITEM_CODIGO VARCHAR(50) NOT NULL UNIQUE,
ITEM_NOMBRE VARCHAR(50) NOT NULL,
ITEM_PRECIO_COMPRA double NOT NULL,
ITEM_PRECIO_VENTA double NOT NULL,
ITEM_UNIDADES double NOT NULL,
PROVEEDOR_ID INT NOT NULL,
ITEM_TIPO_ID INT NOT NULL,
ITEM_CATEGORIA_ID INT NOT NULL,
ITEM_ESTADO VARCHAR(25) not null)

CREATE TABLE ITEM_TIPO(
ITEM_TIPO_ID INT NOT NULL PRIMARY KEY auto_increment,
ITEM_TIPO_NOMBRE VARCHAR(50) NOT NULL,
ITEM_TIPO_ESTADO VARCHAR(25) not null)


CREATE TABLE INVENTARIO(
INVENTARIO_ID INT NOT NULL PRIMARY KEY auto_increment,
ITEM_ID INT NOT NULL,
INVENTARIO_CODIGO_BARRA VARCHAR(80) DEFAULT NULL,
INVENTARIO_UBICACION VARCHAR(80) DEFAULT NULL,
INVENTARIO_CANTIDAD INT NOT NULL,
INVENTARIO_SALDO INT NOT NULL,
INVENTARIO_FECHA_INGRESO DATETIME NOT NULL,
INVENTARIO_FECHA_FABRICACION DATE NOT NULL,
INVENTARIO_FECHA_VENCIMIENTO DATE NOT NULL,
INVENTARIO_ESTADO VARCHAR(25) NOT NULL
)
SELECT *FROM INVENTARIO

