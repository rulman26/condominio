PROVEEDOR_RUC VARCHAR(12) NOT NULL,
PROVEEDOR_NOMBRE VARCHAR(180) NOT NULL,
PROVEEDOR_DIRECCION VARCHAR(250) NOT NULL,
PROVEEDOR_TELEFONO VARCHAR(12) default NULL,
PROVEEDOR_EMAIL VARCHAR(50) default NULL,
PROVEEDOR_ESTADO VARCHAR(25) not null)

