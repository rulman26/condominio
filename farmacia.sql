CREATE  TABLE USER (
ID INT NOT NULL PRIMARY KEY auto_increment,
PERFIL VARCHAR(150) NOT NULL,
USUARIO VARCHAR(50) NOT NULL unique,
PASSWORD VARCHAR(30) NOT NULL,
NOMBRES VARCHAR(150) NOT NULL,
TOKEN VARCHAR(150) NOT NULL,
ESTADO VARCHAR(25) not null)

CREATE TABLE CLIENTE(
CLIENTE_ID INT NOT NULL PRIMARY KEY auto_increment,
CLIENTE_NOMBRE VARCHAR(180) NOT NULL,
CLIENTE_DIRECCION VARCHAR(250) NOT NULL,
CLIENTE_DNI VARCHAR(8) default NULL UNIQUE,
CLIENTE_RUC VARCHAR(12) default NULL UNIQUE,
CLIENTE_TELEFONO VARCHAR(12) default NULL,
CLIENTE_ESTADO VARCHAR(12) default NULL
)
select *from CLIENTE

CREATE TABLE PROVEEDOR(
